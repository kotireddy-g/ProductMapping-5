export const getVendorSummary = () => {
  const totalVendors = vendorForecastData.length;
  const overstockValue = vendorForecastData.reduce((sum, v) => sum + (v.overstock * 40), 0);
  const understockRisk = vendorForecastData.reduce((sum, v) => sum + (v.understock * 40), 0);
  const avgOtif = Math.round(vendorForecastData.reduce((sum, v) => sum + v.otif, 0) / totalVendors);
  
  return {
    totalVendors,
    overstockValue,
    understockRisk,
    avgOtif
  };
};

export const getStatusColor = (status) => {
  switch (status) {
    case 'reliable': return { bg: 'bg-green-100', text: 'text-green-700', border: 'border-green-300' };
    case 'warning': return { bg: 'bg-yellow-100', text: 'text-yellow-700', border: 'border-yellow-300' };
    case 'critical': return { bg: 'bg-red-100', text: 'text-red-700', border: 'border-red-300' };
    default: return { bg: 'bg-slate-100', text: 'text-slate-700', border: 'border-slate-300' };
  }
};

export const getOtifColor = (otif) => {
  if (otif >= 90) return 'text-green-600 bg-green-100';
  if (otif >= 80) return 'text-yellow-600 bg-yellow-100';
  return 'text-red-600 bg-red-100';
};